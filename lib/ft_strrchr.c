char	*ft_strrchr(const char *str, int c)
{
	char	*temp;
	char	*s;

	temp = 0;
	s = (char *)str;
	if (c == 1024)
		c = '\0';
	if (c >= 256 && c < 1024)
		c = (c - 256);
	if (c > 1024)
		c = (c - 1024);
	while (*s != '\0')
	{
		if (*s == c)
			temp = s;
		s++;
	}
	if (c == '\0')
		return (s);
	return (temp);
}
